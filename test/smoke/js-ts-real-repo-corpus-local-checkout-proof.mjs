import assert from 'node:assert/strict';
import { mkdirSync, mkdtempSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { assertRealRepoCorpusMetrics } from './js-ts-real-repo-corpus-assertions.mjs';
import { measureRealRepoCorpus, readRealRepoCorpusFixture } from '../../bench/real-repo-corpus-suite.mjs';

const corpus = readRealRepoCorpusFixture();
const manifest = corpus.realRepoCorpus;
const proof = manifest.localBehavior?.checkoutProof ?? {};
const envName = proof.rootEnv ?? 'FRONTIER_REAL_REPO_CORPUS_ROOT';
const previousRoot = process.env[envName];
const tempRoot = mkdtempSync(join(tmpdir(), 'frontier-real-repo-corpus-'));

try {
  const viteEntry = manifest.entries.find((entry) => entry.id === 'vite-plugin-config');
  mkdirSync(join(tempRoot, 'vite', 'packages/vite/src/node'), { recursive: true });
  writeFileSync(join(tempRoot, 'vite', 'packages/vite/src/node/config.ts'), 'export const syntheticConfig = true;\n');
  writeFileSync(join(tempRoot, 'vite', 'pnpm-lock.yaml'), 'lockfileVersion: 9\n');
  mkdirSync(join(tempRoot, 'vite', '.git'), { recursive: true });
  writeFileSync(join(tempRoot, 'vite', '.git/HEAD'), `ref: refs/heads/${viteEntry.source.ref}\n`);
  writeFileSync(join(tempRoot, 'vite', '.git/config'), `[remote "origin"]\n\turl = ${viteEntry.source.url}\n`);
  mkdirSync(join(tempRoot, 'react'), { recursive: true });
  writeFileSync(join(tempRoot, 'react', '.git'), 'gitdir: ../react.git\n');

  process.env[envName] = tempRoot;
  const metrics = measureRealRepoCorpus();
  assertRealRepoCorpusMetrics(metrics, manifest, assert);
  assert.equal(metrics.realRepoCorpusCheckoutRootMode, 'env-override', 'synthetic checkout root mode');
  assert.equal(metrics.realRepoCorpusExternalRootPresent, true, 'synthetic checkout root present');
  assert.equal(metrics.realRepoCorpusCheckoutProofExecuted, 2, 'synthetic checkout proof executed rows');
  assert.equal(metrics.realRepoCorpusCheckoutProofSkipped, (manifest.entries ?? []).length - 2, 'synthetic skipped rows');
  assert.equal(metrics.realRepoCorpusCheckoutCheckedOut, 2, 'synthetic checked-out accounting');
  assert.equal(metrics.realRepoCorpusCheckoutProofMatchedEntries, 1, 'synthetic matched checkout entries');
  assert.equal(metrics.realRepoCorpusCheckoutNoProofMatch, 1, 'synthetic no-proof-match checkout entries');
  assert.equal(metrics.realRepoCorpusCheckoutDirPresentRows, 2, 'synthetic checkout dir present rows');
  assert.equal(metrics.realRepoCorpusCheckoutRootMissingRows, 0, 'synthetic checkout root missing rows');
  assert.equal(metrics.realRepoCorpusCheckoutRootUnconfiguredRows, 0, 'synthetic checkout root unconfigured rows');
  assert.equal(metrics.realRepoCorpusCheckoutDirMissingRows, (manifest.entries ?? []).length - 2, 'synthetic checkout dir missing rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentityExecuted, 2, 'synthetic checkout identity executed rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentitySkipped, (manifest.entries ?? []).length - 2, 'synthetic identity skipped rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentityMetadataPresent, 2, 'synthetic checkout git metadata rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentityMatched, 1, 'synthetic checkout identity matched rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentityRemoteMatched, 1, 'synthetic checkout remote matched rows');
  assert.equal(metrics.realRepoCorpusCheckoutIdentityRefMatched, 1, 'synthetic checkout ref matched rows');
  assert.equal(metrics.realRepoCorpusCheckoutGitDirectories, 1, 'synthetic checkout git directory rows');
  assert.equal(metrics.realRepoCorpusCheckoutGitDirPointers, 1, 'synthetic checkout gitdir pointer rows');
  assert.equal(metrics.realRepoCorpusCheckoutGitConfigsPresent, 1, 'synthetic checkout git config rows');
  assert.equal(metrics.realRepoCorpusCheckoutGitOriginUrlsPresent, 1, 'synthetic checkout git origin rows');
  assert.equal(metrics.realRepoCorpusRepositoryCommandsRun, 0, 'synthetic checkout repository commands remain disabled');
  assert.equal(metrics.realRepoCorpusDependencyInstallsRun, 0, 'synthetic checkout dependency installs remain disabled');
  assert.equal(metrics.realRepoCorpusCheckoutLockfileMetadataRows, 1, 'synthetic checkout lockfile metadata rows');

  const rowsByEntry = new Map(metrics.realRepoCorpusCheckoutEvidenceRows.map((row) => [row.entryId, row]));
  const viteRow = rowsByEntry.get('vite-plugin-config');
  assert.equal(viteRow?.checkoutProofStatus, 'checked-out', 'synthetic Vite checkout proof status');
  assert.equal(viteRow.checkoutProofExecution, 'executed', 'synthetic Vite checkout proof execution');
  assert.equal(viteRow.checkoutRootMode, 'env-override', 'synthetic Vite checkout root mode');
  assert.equal(viteRow.checkoutRootPresent, true, 'synthetic Vite checkout root present');
  assert.equal(viteRow.checkoutDirPresent, true, 'synthetic Vite checkout dir present');
  assert.equal(viteRow.checkoutPresenceStatus, 'checkout-dir-present', 'synthetic Vite checkout presence status');
  assert.equal(viteRow.checkoutProofReason, 'declared-proof-globs-matched', 'synthetic Vite checkout proof reason');
  assert.equal(viteRow.dependencyInstallProofStatus, 'lockfile-metadata-present', 'synthetic Vite dependency metadata');
  assert.equal(viteRow.dependencyInstallExecution, proof.dependencyInstallExecution, 'synthetic Vite install execution');
  assert.equal(viteRow.dependencyInstallOptInRequired, true, 'synthetic Vite dependency install opt-in');
  assert.deepEqual(viteRow.packageManagerLockFiles, ['pnpm-lock.yaml']);
  assert.deepEqual(viteRow.packageManagersPresent, ['pnpm']);
  assert.equal(viteRow.packageManagerCommandMatrixStatus, 'metadata-only', 'synthetic Vite command matrix status');
  assert.equal(viteRow.packageManagerCommandMatrixCommands, 9, 'synthetic Vite command matrix command count');
  assert.equal(viteRow.repositoryCommandExecution, proof.repositoryCommandExecution, 'synthetic Vite command execution');
  assert.equal(viteRow.repositoryCommandOptInRequired, true, 'synthetic Vite repository command opt-in');
  assert.equal(viteRow.checkoutIdentityStatus, 'git-identity-matched', 'synthetic Vite checkout identity status');
  assert.equal(viteRow.checkoutIdentityExecution, 'executed', 'synthetic Vite checkout identity execution');
  assert.equal(viteRow.gitMetadataPresent, true, 'synthetic Vite git metadata present');
  assert.equal(viteRow.gitMetadataKind, 'git-directory', 'synthetic Vite git metadata kind');
  assert.equal(viteRow.gitDirPointerPresent, false, 'synthetic Vite gitdir pointer absent');
  assert.equal(viteRow.gitHeadPresent, true, 'synthetic Vite git HEAD metadata present');
  assert.equal(viteRow.gitConfigPresent, true, 'synthetic Vite git config present');
  assert.equal(viteRow.gitRemoteOriginUrlPresent, true, 'synthetic Vite git origin present');
  assert.equal(viteRow.gitRemoteOriginMatchesManifest, true, 'synthetic Vite remote matches manifest');
  assert.equal(viteRow.gitRefMatchesManifest, true, 'synthetic Vite ref matches manifest');
  assert.equal(viteRow.matchedFiles >= 1, true, 'synthetic Vite matched file stats');
  assert.equal(viteRow.sourceTextRead, false, 'synthetic Vite source text access');
  assert.equal(viteRow.commandDryRunStatus, 'ready-local-checkout', 'synthetic Vite command dry-run readiness');
  assert.equal(viteRow.commandDryRunExecutionStatus, 'opt-in-required', 'synthetic Vite command dry-run execution');
  assert.equal(viteRow.commandDryRunPhaseCount, 3, 'synthetic Vite command dry-run phase count');
  assert.deepEqual(viteRow.commandDryRunPhases.map((phase) => phase.phase), ['dependency-install', 'build', 'test']);
  assert.deepEqual(viteRow.commandDryRunPhases.map((phase) => phase.readinessStatus), ['ready-local-checkout', 'ready-local-checkout', 'ready-local-checkout']);
  assert.deepEqual(viteRow.commandDryRunPhases.map((phase) => phase.executionStatus), ['opt-in-required', 'opt-in-required', 'opt-in-required']);
  assert.equal(viteRow.commandDryRunReadyPhases, 3, 'synthetic Vite command dry-run ready phases');
  assert.equal(viteRow.commandDryRunOptInRequiredPhases, 3, 'synthetic Vite command dry-run opt-in phases');
  assert.equal(viteRow.commandDryRunExecutedPhases, 0, 'synthetic Vite command dry-run executed phases');

  const reactRow = rowsByEntry.get('react-component-patterns');
  assert.equal(reactRow?.checkoutProofStatus, 'checked-out-no-proof-match', 'synthetic React no-proof-match status');
  assert.equal(reactRow.checkoutProofExecution, 'executed', 'synthetic React checkout proof execution');
  assert.equal(reactRow.checkoutRootMode, 'env-override', 'synthetic React checkout root mode');
  assert.equal(reactRow.checkoutRootPresent, true, 'synthetic React checkout root present');
  assert.equal(reactRow.checkoutDirPresent, true, 'synthetic React checkout dir present');
  assert.equal(reactRow.checkoutPresenceStatus, 'checkout-dir-present', 'synthetic React checkout presence status');
  assert.equal(reactRow.checkoutProofReason, 'declared-proof-globs-missing', 'synthetic React checkout proof reason');
  assert.equal(reactRow.checkoutIdentityStatus, 'gitdir-pointer-present', 'synthetic React identity gitdir pointer');
  assert.equal(reactRow.checkoutIdentityExecution, 'executed', 'synthetic React identity execution');
  assert.equal(reactRow.gitMetadataPresent, true, 'synthetic React git metadata present');
  assert.equal(reactRow.gitMetadataKind, 'gitdir-pointer', 'synthetic React git metadata kind');
  assert.equal(reactRow.gitDirPointerPresent, true, 'synthetic React gitdir pointer present');
  assert.equal(reactRow.gitConfigPresent, false, 'synthetic React git config absent');
  assert.equal(reactRow.gitRemoteOriginUrlPresent, false, 'synthetic React git origin absent');
  assert.equal(reactRow.dependencyInstallProofStatus, 'lockfile-metadata-missing', 'synthetic React dependency metadata missing');
  assert.equal(reactRow.commandDryRunStatus, 'ready-local-checkout', 'synthetic React command dry-run readiness');
  assert.equal(reactRow.commandDryRunExecutionStatus, 'opt-in-required', 'synthetic React command dry-run execution');
  assert.equal(reactRow.commandDryRunReadyPhases, 3, 'synthetic React command dry-run ready phases');
  assert.equal(reactRow.commandDryRunOptInRequiredPhases, 3, 'synthetic React command dry-run opt-in phases');
  assert.equal(reactRow.matchedFiles, 0, 'synthetic React no-proof-match file stats');

  const typescriptRow = rowsByEntry.get('typescript-compiler-services');
  assert.equal(typescriptRow?.checkoutProofStatus, 'skipped-missing-checkout', 'synthetic missing checkout status');
  assert.equal(typescriptRow.checkoutProofExecution, 'skipped', 'synthetic missing checkout proof execution');
  assert.equal(typescriptRow.checkoutRootMode, 'env-override', 'synthetic missing checkout root mode');
  assert.equal(typescriptRow.checkoutRootPresent, true, 'synthetic missing checkout root present');
  assert.equal(typescriptRow.checkoutDirPresent, false, 'synthetic missing checkout dir absent');
  assert.equal(typescriptRow.checkoutPresenceStatus, 'checkout-dir-missing', 'synthetic missing checkout presence status');
  assert.equal(typescriptRow.checkoutProofReason, 'checkout-dir-missing', 'synthetic missing checkout proof reason');
  assert.equal(typescriptRow.checkoutIdentityStatus, 'skipped-missing-checkout', 'synthetic missing checkout identity status');
  assert.equal(typescriptRow.checkoutIdentityExecution, 'skipped', 'synthetic missing checkout identity execution');
  assert.equal(typescriptRow.dependencyInstallProofStatus, 'skipped-missing-checkout', 'synthetic missing dependency proof');
  assert.equal(typescriptRow.commandDryRunStatus, 'skipped-missing-checkout', 'synthetic missing command dry-run readiness');
  assert.equal(typescriptRow.commandDryRunExecutionStatus, 'skipped-missing-checkout', 'synthetic missing command dry-run execution');
  assert.equal(typescriptRow.commandDryRunSkippedPhases, 3, 'synthetic missing command dry-run skipped phases');
  assert.equal(typescriptRow.commandDryRunReadyPhases, 0, 'synthetic missing command dry-run ready phases');
  assert.equal(typescriptRow.commandDryRunOptInRequiredPhases, 0, 'synthetic missing command dry-run opt-in phases');
} finally {
  if (previousRoot === undefined) delete process.env[envName];
  else process.env[envName] = previousRoot;
  rmSync(tempRoot, { recursive: true, force: true });
}
